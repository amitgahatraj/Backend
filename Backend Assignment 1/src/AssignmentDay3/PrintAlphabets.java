//30.Java program to print all alphabets from a to z using for loop.

package AssignmentDay3;
public class PrintAlphabets {
    public static void main(String[] args) {
        System.out.println("Alphabets from 'a' to 'z':");

        for (char c = 'a'; c <= 'z'; c++) {
            System.out.print(c + " ");
        }
    }
}

